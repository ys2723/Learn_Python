# While Loop

a = 0 
while a<10:
    print("Yes")
    a = a + 1
    
print("\nOkay\n")
    
b = 0
while b<10:
    print("BEE" + " " + str(b))
    b = b + 1
    
print("\n")

capitals = ['Madrid','Canberra','Hanoi','Brasilia','Ottawa']
c = 0
while c < len(capitals):
    print(capitals[c])
    c = c + 1

# Observe that the use of braces isn't really required but indentation (spaces) is important.
